class Product {
    constructor(id, title, imageUrl, type, description, amount, price, quantity, sum) {
        this.id = id;
        this.title = title;
        this.imageUrl = imageUrl;
        this.type = type;
        this.description = description;
        this.amount = amount;
        this.price = price;
        this.quantity = quantity;
        this.sum = sum;
    }
}

export default Product;